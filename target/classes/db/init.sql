-- 开票中间表主表（海典写入，字段与金蝶接口完全对应）
CREATE TABLE IF NOT EXISTS `t_sim_original_bill` (
    `id`                      VARCHAR(50)   NOT NULL                COMMENT '主键',
    `bill_no`                 VARCHAR(50)                           COMMENT '单据编号',
    `bill_date`               VARCHAR(20)                           COMMENT '单据日期 yyyy-MM-dd',
    `invoice_property`        VARCHAR(10)                           COMMENT '发票性质：0-蓝票；1-红票',
    `invoice_type`            VARCHAR(20)                           COMMENT '发票种类：028/026/004/007/025/08xdp/10xdp',
    `remark`                  VARCHAR(230)                          COMMENT '备注',
    `buyer_name`              VARCHAR(100)                          COMMENT '购买方名称',
    `buyer_taxpayer_id`       VARCHAR(50)                           COMMENT '购方税号（专票必传）',
    `buyer_bank_and_account`  VARCHAR(200)                          COMMENT '购方银行和账号',
    `buyer_address_and_tel`   VARCHAR(100)                          COMMENT '购方地址电话',
    `buyer_recipient_mail`    VARCHAR(200)                          COMMENT '购方收件邮箱，多个用英文分号分隔',
    `buyer_property`          VARCHAR(5)                            COMMENT '购买方性质：0-企业，1-个人，2-非企业单位',
    `org_code`                VARCHAR(20)                           COMMENT '销方组织编码',
    `seller_name`             VARCHAR(100)                          COMMENT '销方名称',
    `seller_bank_and_account` VARCHAR(150)                          COMMENT '销方银行和账号',
    `seller_address_and_tel`  VARCHAR(120)                          COMMENT '销方地址和电话',
    `total_amount`            DECIMAL(18,2)                         COMMENT '单据金额，根据含税标记区分',
    `include_tax_flag`        VARCHAR(5)                            COMMENT '含税标识：0-不含税，1-含税',
    `blueinvoice_code`        VARCHAR(200)                          COMMENT '待冲蓝票代码',
    `blueinvoice_no`          VARCHAR(200)                          COMMENT '待冲蓝票号码',
    `red_reason`              VARCHAR(10)                           COMMENT '冲红原因：1-销货退回；2-开票有误；3-开票中...',
    `original_issue_time`     VARCHAR(20)                           COMMENT '待冲蓝票开票日期 yyyy-MM-dd',
    `blue_invoice_type`       VARCHAR(20)                           COMMENT '待冲蓝票类型',
    `auto_invoice`            VARCHAR(5)                            COMMENT '自动开票标记：0-否，1-是',
    `username`                VARCHAR(50)                           COMMENT '用户手机号',
    `tu25_producttype`        VARCHAR(5)                            COMMENT '产品类型：0-药品类，1-其他',
    `tu25_productbatchno`     VARCHAR(100)                          COMMENT '产品批号',
    `tu25_producedate`        VARCHAR(20)                           COMMENT '生产日期',
    `tu25_validperiod`        VARCHAR(50)                           COMMENT '有效期',
    `tu25_productorigin`      VARCHAR(100)                          COMMENT '产地',
    `tu25_provinceserialno`   VARCHAR(100)                          COMMENT '省标流水号',
    `drawer`                  VARCHAR(50)                           COMMENT '开票人',
    `is_invoicing`            VARCHAR(5)   NOT NULL DEFAULT '0'     COMMENT '是否已开票：0-未开票，1-已开票，2-开票失败',
    -- 开票后返回字段
    `interface_code`          VARCHAR(20)                           COMMENT '业务编码，开票回调是INVOICE.OPEN',
    `return_code`             VARCHAR(20)                           COMMENT '返回编码：0-成功，9999-失败',
    `return_msg`              VARCHAR(500)                          COMMENT '返回信息，失败原因或success',
    `invoice_status`          VARCHAR(20)                           COMMENT '发票状态：0-正常，2-部分红冲，3-红冲，6-作废',
    `invoice_code`            VARCHAR(50)                           COMMENT '发票代码',
    `invoice_num`             VARCHAR(50)                           COMMENT '发票号码',
    `invoice_date`            VARCHAR(20)                           COMMENT '开票日期',
    `invoice_file_url`        VARCHAR(200)                          COMMENT '版式文件下载地址（数电票为ofd）',
    `invoice_image_url`       VARCHAR(200)                          COMMENT 'PDF转图片预览地址',
    `invoice_pdf_file_url`    VARCHAR(200)                          COMMENT '数电票PDF地址',
    `invoice_xml_file_url`    VARCHAR(200)                          COMMENT '数电票XML地址',
    `order_no`                VARCHAR(50)                           COMMENT '发票流水号',
    `create_time`             DATETIME     NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `update_time`             DATETIME     NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    PRIMARY KEY (`id`),
    KEY `idx_bill_no`      (`bill_no`),
    KEY `idx_is_invoicing` (`is_invoicing`),
    KEY `idx_create_time`  (`create_time`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='海典-金蝶开票中间表主表';

-- 开票中间表明细表
CREATE TABLE IF NOT EXISTS `t_sim_original_bill_item` (
    `id`                VARCHAR(50)   NOT NULL          COMMENT '主键',
    `billid`            VARCHAR(50)   NOT NULL          COMMENT '关联主表id',
    `amount`            DECIMAL(18,2)                   COMMENT '明细金额，含税标识=1时为含税金额',
    `original_seq`      INT                             COMMENT '原蓝票明细行序号，部分红冲时必填',
    `goods_code`        VARCHAR(50)                     COMMENT '商品编码',
    `goods_name`        VARCHAR(92)                     COMMENT '商品名称',
    `line_property`     VARCHAR(5)                      COMMENT '行性质：1-折扣行，2-正常商品行',
    `price`             DECIMAL(18,6)                   COMMENT '单价，含税标识=1时为含税单价',
    `quantity`          DECIMAL(18,6)                   COMMENT '数量',
    `tax_amount`        DECIMAL(18,2)                   COMMENT '税额',
    `tax_rate`          DECIMAL(8,4)                    COMMENT '税率，如0.13、0.09、0.06',
    `privilege_content` VARCHAR(50)                     COMMENT '享受优惠内容：免税/不征税/普通零税率',
    `privilege_flag`    VARCHAR(5)                      COMMENT '是否享受优惠：0-不享受，1-享受',
    `revenue_code`      VARCHAR(50)                     COMMENT '税收分类编码',
    `specification`     VARCHAR(40)                     COMMENT '规格型号',
    `discount_amount`   DECIMAL(18,2)                   COMMENT '折扣金额（负数）',
    `units`             VARCHAR(22)                     COMMENT '计量单位',
    `remark`            VARCHAR(230)                    COMMENT '发票备注',
    `extra_field`       VARCHAR(200)                    COMMENT '合并标识字段（金蝶赋值）',
    `extra_field2`      VARCHAR(200)                    COMMENT '拆分分组字段（金蝶赋值）',
    PRIMARY KEY (`id`),
    KEY `idx_billid` (`billid`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='海典-金蝶开票中间表明细';
